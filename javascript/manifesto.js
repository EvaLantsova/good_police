var manifesto_book_el = document.getElementById('manifesto_book');

window.addEventListener('resize', function(e) {
  manifesto_book_el.style.width = '';
  manifesto_book_el.style.height = '';
  $(manifesto_book_el).turn('size', manifesto_book_el.clientWidth, manifesto_book_el.clientHeight);
});

 $(manifesto_book_el).turn({
 autoCenter: true
})
